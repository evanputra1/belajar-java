// 'com.tutorial' adalah nama paket (package) di sini.
package com.tutorial;

// Ini adalah deklarasi kelas utama bernama 'Main'.
public class Main {

    // Ini adalah metode (fungsi) utama yang akan dijalankan ketika program dimulai.
    public static void main(String[] args) {

        // Ini mencetak satu baris kosong
        System.out.println("\n");

        // Ini mendeklarasikan variabel 'x' dengan tipe data integer dan memberi nilainya 10.
        int x = 10;
        // Ini mencetak teks "nilai x = " diikuti dengan nilai dari variabel 'x'.
        System.out.println("nilai x = " + x);

        // Ini mendeklarasikan variabel 'y' dengan tipe data float dan memberi nilainya 5.99.
        float y = 5.99f;
        // Ini mencetak teks "nilai float y adalah = " diikuti dengan nilai dari variabel 'y'.
        System.out.println("nilai float y adalah = " + y);

        // Ini mendeklarasikan variabel 'letter' dengan tipe data char (karakter) dan memberi nilainya 'D'.
        char letter = 'D';
        // Ini mencetak teks "huruf depan Dodi adalah = " diikuti dengan nilai dari variabel 'letter'.
        System.out.println("huruf depan Dodi adalah = " + letter);

        // Ini mendeklarasikan variabel 'bool' dengan tipe data boolean dan memberi nilainya 'true'.
        boolean bool = true;
        // Ini mencetak teks "apakah 11 > 10 = " diikuti dengan nilai dari variabel 'bool'.
        System.out.println("apakah 11 > 10 = " + bool);

        // Ini mendeklarasikan variabel 'text' dengan tipe data String (teks) dan memberi nilainya "Udin".
        String text = "Udin";
        // Ini mencetak teks "halo nama saya " diikuti dengan nilai dari variabel 'text'.
        System.out.println("halo nama saya " + text);

        // Ini mendeklarasikan variabel 'z' dengan tipe data double dan memberi nilainya 3.14.
        double z = 3.14;
        // Ini mencetak teks "nilai di pi adalah = " diikuti dengan nilai dari variabel 'z'.
        System.out.println("nilai di pi adalah = " + z);

        // Ini adalah deklarasi variabel 'a' dengan tipe data int (integer).
        int a;
        // Ini adalah memberikan nilai 8 ke variabel 'a'.
        a = 8;
        // Ini adalah perintah untuk mencetak teks "nilai a = " diikuti dengan nilai dari variabel 'a' ke konsol.
        System.out.println("nilai a = " + a);

        // Ini mencetak satu baris kosong
        System.out.println("\n");
    }
}
